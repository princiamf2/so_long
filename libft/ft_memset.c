#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	unsigned char	*s2;
	size_t			i;

	s2 = str;
	i = 0;
	while (i < n)
	{
		s2[i] = c;
		i++;
	}
	return (str);
}
